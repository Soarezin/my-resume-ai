<script setup>
import { ref, computed } from 'vue'
import { useRouter } from 'vue-router'
import { useAuthStore } from '../stores/authStore'

const router = useRouter()
const authStore = useAuthStore()

const email = ref('')
const password = ref('')
const confirmPassword = ref('')
const error = ref('')
const loading = ref(false)

const isFormValid = computed(() => {
  return email.value && 
         password.value && 
         confirmPassword.value && 
         password.value === confirmPassword.value &&
         password.value.length >= 6
})

async function handleRegister() {
  if (!isFormValid.value) {
    error.value = 'Por favor, preencha todos os campos corretamente'
    return
  }

  if (password.value !== confirmPassword.value) {
    error.value = 'As senhas não coincidem'
    return
  }

  loading.value = true
  error.value = ''

  try {
    const { error: registerError } = await authStore.register(email.value, password.value)
    if (registerError) throw registerError
    router.push('/dashboard')
  } catch (err) {
    error.value = 'Erro ao criar conta. Verifique seus dados e tente novamente.'
    console.error('Erro no registro:', err)
  } finally {
    loading.value = false
  }
}

async function handleGoogleLogin() {
  loading.value = true
  error.value = ''

  try {
    const { error: loginError } = await authStore.loginWithGoogle()
    if (loginError) throw loginError
  } catch (err) {
    error.value = 'Erro ao fazer login com Google'
    console.error('Erro no login com Google:', err)
  } finally {
    loading.value = false
  }
}

async function handleGithubLogin() {
  loading.value = true
  error.value = ''

  try {
    const { error: loginError } = await authStore.loginWithGithub()
    if (loginError) throw loginError
  } catch (err) {
    error.value = 'Erro ao fazer login com GitHub'
    console.error('Erro no login com GitHub:', err)
  } finally {
    loading.value = false
  }
}
</script>

